package model;

public class Aeropuerto {
	
	private String codigo,descCiu,descAero;

	public String getCodigo() {
		return codigo;
	}

	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}

	public String getDescCiu() {
		return descCiu;
	}

	public void setDescCiu(String descCiu) {
		this.descCiu = descCiu;
	}

	public String getDescAero() {
		return descAero;
	}

	public void setDescAero(String descAero) {
		this.descAero = descAero;
	}

	
	
}
