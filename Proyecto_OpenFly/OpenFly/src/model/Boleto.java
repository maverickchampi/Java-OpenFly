package model;

public class Boleto {
	private String codBol, codUsu, codVI, codVV;
	private int asientos;
	public String getCodBol() {
		return codBol;
	}
	public void setCodBol(String codBol) {
		this.codBol = codBol;
	}
	public String getCodUsu() {
		return codUsu;
	}
	public void setCodUsu(String codUsu) {
		this.codUsu = codUsu;
	}
	public String getCodVI() {
		return codVI;
	}
	public void setCodVI(String codVI) {
		this.codVI = codVI;
	}
	public String getCodVV() {
		return codVV;
	}
	public void setCodVV(String codVV) {
		this.codVV = codVV;
	}
	public int getAsientos() {
		return asientos;
	}
	public void setAsientos(int asientos) {
		this.asientos = asientos;
	}
	
	
}
